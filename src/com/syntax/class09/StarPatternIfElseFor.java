package com.syntax.class09;

public class StarPatternIfElseFor {

	public static void main(String[] args) {

		for(int x=1; x<=10; x++) {
			for(int y=1; y<=x; y++) {
				System.out.print(y+" ");
			}System.out.println();
		}
		for(int x=9; x>=1; x--) {
			for(int y=1; y<=x; y++) {
				System.out.print(y+" ");
			}System.out.println();
		}
		}
		
	}


