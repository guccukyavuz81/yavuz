package com.syntax.class07;

public class WhileLoopIntro {

	public static void main(String[] args) {

		int time=10;
		if(time<12) {
			System.out.println("hi");
			
			System.out.println("...........................................");
			
			
		}
		
		while(time<=12) {
			System.out.println("hi");
			time++;
		}
	}

}
