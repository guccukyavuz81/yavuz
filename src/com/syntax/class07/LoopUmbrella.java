package com.syntax.class07;

public class LoopUmbrella {

	public static void main(String[] args) {

		
		boolean rain=true;
		if(rain) {
			System.out.println("umbrella");
		}
		System.out.println("........................");
		
		while(rain) {
			System.out.println("umbrella");
			rain=false;
		}
		
		
	}

}
